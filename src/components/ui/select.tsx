interface SelectContentProps extends React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content> {
  searchable?: boolean;
  searchPlaceholder?: string;
}

const extractText = (child: React.ReactNode): string => {
  if (typeof child === "string" || typeof child === "number") return String(child)
  if (Array.isArray(child)) return child.map(extractText).join(" ")
  if (React.isValidElement(child)) return extractText(child.props.children)
  return ""
}

const SelectContent = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Content>,
  SelectContentProps
>(({ className, children, position = "popper", searchable = true, searchPlaceholder = "Search...", ...props }, ref) => {
  const [searchTerm, setSearchTerm] = React.useState("")

  // Filter children based on search term
  const filteredChildren = React.useMemo(() => {
    if (!searchable || !searchTerm) return children

    return React.Children.toArray(children).filter((child) => {
      if (React.isValidElement(child)) {
        const searchStrings: string[] = []

        // Collect inner text (recursively) from children
        searchStrings.push(extractText(child.props.children))

        // Add `value` if string/number
        if (child.props?.value) {
          searchStrings.push(String(child.props.value))
        }

        // Combine all search content
        const haystack = searchStrings.join(" ").toLowerCase()
        return haystack.includes(searchTerm.toLowerCase())
      }

      return true
    })
  }, [children, searchTerm, searchable])

  const handleSearchChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    e.preventDefault()
    e.stopPropagation()
    setSearchTerm(e.target.value)
  }

  const handleSearchKeyDown = (e: React.KeyboardEvent) => {
    // Prevent the select from closing when typing
    e.stopPropagation()
  }

  return (
    <SelectPrimitive.Portal>
      <SelectPrimitive.Content
        ref={ref}
        className={cn(
          "relative z-50 max-h-96 min-w-[8rem] overflow-hidden rounded-md border bg-popover text-popover-foreground shadow-md data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2",
          position === "popper" &&
            "data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",
          className
        )}
        position={position}
        {...props}
      >
        <SelectScrollUpButton />
        {searchable && (
          <div className="flex items-center border-b px-3 py-2">
            <Search className="mr-2 h-4 w-4 shrink-0 opacity-50" />
            <Input
              placeholder={searchPlaceholder}
              value={searchTerm}
              onChange={handleSearchChange}
              onKeyDown={handleSearchKeyDown}
              className="flex h-8 w-full rounded-md border-0 bg-transparent py-1 text-sm outline-none placeholder:text-muted-foreground focus:outline-none disabled:cursor-not-allowed disabled:opacity-50"
              autoFocus
            />
          </div>
        )}
        <SelectPrimitive.Viewport
          className={cn(
            "p-1",
            position === "popper" &&
              "h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)]"
          )}
        >
          {searchable ? filteredChildren : children}
        </SelectPrimitive.Viewport>
        <SelectScrollDownButton />
      </SelectPrimitive.Content>
    </SelectPrimitive.Portal>
  )
})
SelectContent.displayName = SelectPrimitive.Content.displayName

